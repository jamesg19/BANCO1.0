package ReporteClass;

/**
 *
 * @author james
 */
public class Reporte7Gerente {
    private String codigo;
    private String nombre;
    private String dpi;
    private String direccion;
    private String totalTransaccion;

    public Reporte7Gerente() {
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDpi() {
        return dpi;
    }

    public void setDpi(String dpi) {
        this.dpi = dpi;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTotalTransaccion() {
        return totalTransaccion;
    }

    public void setTotalTransaccion(String totalTransaccion) {
        this.totalTransaccion = totalTransaccion;
    }
    
    
    
}
